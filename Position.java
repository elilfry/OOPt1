public class Position {


    //data
private int x,y;

    //constructor

    public Position(int x,int y){
        this.x=x;
        this.y=y;
    }
    //function

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }



}
